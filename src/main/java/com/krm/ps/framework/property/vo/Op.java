package com.krm.ps.framework.property.vo;

public class Op {

	private Long proId;
	private String proName;
	private String proValue;
	private String proComment;
	private String organCode;
	private Long opProId;
	private String opValue;
	public Long getProId() {
		return proId;
	}
	public void setProId(Long proId) {
		this.proId = proId;
	}
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public String getProValue() {
		return proValue;
	}
	public void setProValue(String proValue) {
		this.proValue = proValue;
	}
	public String getProComment() {
		return proComment;
	}
	public void setProComment(String proComment) {
		this.proComment = proComment;
	}
	public String getOrganCode() {
		return organCode;
	}
	public void setOrganCode(String organCode) {
		this.organCode = organCode;
	}

	public Long getOpProId() {
		return opProId;
	}
	public void setOpProId(Long opProId) {
		this.opProId = opProId;
	}
	public String getOpValue() {
		return opValue;
	}
	public void setOpValue(String opValue) {
		this.opValue = opValue;
	}
	
}
